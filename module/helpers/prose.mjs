/**
 * Project: Anime — "Codex prose": rich, hand-authored item descriptions in the system's own
 * voice. The GM writes a description in a plain textarea using light markup (headings, dividers,
 * lists, bold **Labels**, and the outcome blocks the system actually uses — Hit / Miss / Contest /
 * Threshold / Wound) and it renders as a formatted card that fills the View tab and the chat card.
 * Inline styling lives in `styleManualRules` (numbers auto-blue, `backtick` blue, ~tilde~ gold,
 * Foundry [[rolls]]/@links preserved). NOT a copy of any other system — the block vocabulary maps
 * to Project: Anime's V2 rules.
 *
 * Legacy descriptions were stored as HTML (ProseMirror). Those are detected and passed straight
 * through untouched (backward-compatible); `htmlToMarkup` converts them to editable markup when the
 * GM opens the editor. Everything here is pure/sync except the enriched wrapper and the localized
 * outcome inserts. No Foundry globals are touched at module scope.
 */
import {
  techniqueDie, contestTarget, skillDieSpecs, actorTalents, rangeLabel, physicalRangeLabel,
  modifierTakes, skillTarget, skillDuration, modifierValue, rangeHasTiles, getTalent
} from "./config.mjs";

/** Outcome labels the system uses (rules doc V2) → the color role their bold label wears.
 *  Anything not listed renders as a neutral stat label (Range / Target / Energy / …). */
const OUTCOME_CLASS = {
  hit: "pa-out-hit", miss: "pa-out-miss",
  contest: "pa-out-contest", contested: "pa-out-contest", won: "pa-out-hit", win: "pa-out-hit",
  lost: "pa-out-miss", lose: "pa-out-miss", fail: "pa-out-miss", failure: "pa-out-miss",
  tie: "pa-out-neutral",
  threshold: "pa-out-threshold",
  wound: "pa-out-wound", wounded: "pa-out-wound"
};

/** A description already holds block HTML (a legacy ProseMirror value) — render it as-is. */
export function isLegacyHTML(raw) {
  return /<(p|div|ul|ol|h[1-6]|hr|table|blockquote|section)\b/i.test(String(raw ?? ""));
}

const numSpan = (v) => `<span class="pa-rules-num">${v}</span>`;
const impSpan = (v) => `<span class="pa-rules-improve">${v}</span>`;

/** Inline-style one line of prose markup: HTML-escapes the text, then **bold**, `blue`, ~gold~,
 *  and bare numbers → styled spans (preserving [[rolls]] / @links for a later enrich pass).
 *  Sync. All emitted tags are ours, so a literal "<x>" in the input renders as text. */
export function styleManualRules(raw) {
  let s = String(raw ?? "");
  if (!s.trim()) return "";
  // Escape the author's literal text FIRST, so a stray "<x>" renders as text instead of being
  // swallowed as an unknown tag. Every tag emitted below is generated by us, never from the input.
  s = s.replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;");
  // Stashed fragments hide behind a fake `<pakeep N>` tag: the number-colorize pass skips it as
  // an HTML tag, so its index digits are never mistaken for a rules number.
  const stash = [];
  const keep = (html) => `<pakeep ${stash.push(html) - 1}>`;
  // Preserve Foundry enricher tokens so their inner numbers aren't colorized/split.
  s = s.replace(/\[\[[^\]]*\]\](?:\{[^}]*\})?/g, (m) => keep(m));
  s = s.replace(/@\w+\[[^\]]*\](?:\{[^}]*\})?/g, (m) => keep(m));
  // Manual highlights: `blue` and ~gold~ (button-inserted or typed).
  s = s.replace(/~([^~\n]+)~/g, (_m, t) => keep(impSpan(t)));
  s = s.replace(/`([^`\n]+)`/g, (_m, t) => keep(numSpan(t)));
  // Inline bold: **text** -> <strong> (numbers inside still color, since it's stashed whole).
  s = s.replace(/\*\*(\S[\s\S]*?)\*\*/g, (_m, t) => keep(`<strong>${t}</strong>`));
  // Auto-blue the bare numbers that remain; an HTML tag (incl. the sentinel) passes through.
  s = s.replace(/<[^>]+>|(\bd\d+\b|[+\-−]?\d+%?)/g, (m, num) => (num ? numSpan(num) : m));
  // Restore stashed fragments; loop so a token nested inside a highlight also resolves.
  for (let pass = 0; pass <= stash.length && s.includes("<pakeep "); pass++) {
    s = s.replace(/<pakeep (\d+)>/g, (_m, i) => stash[Number(i)] ?? "");
  }
  return s;
}

/** Inline pass for one line of text: the shared styler escapes it, then applies **bold**,
 *  `blue`, ~gold~, auto-blue numbers, and preserves [[rolls]] / @links. */
function inline(text) {
  const t = String(text ?? "");
  return t.trim() ? styleManualRules(t) : "";
}

/** Render the light markup of a description into the `.pa-desc-prose` block. Sync + pure. */
export function renderProse(raw) {
  const src = String(raw ?? "").replace(/\r\n?/g, "\n");
  if (!src.trim()) return "";

  const out = [];
  let para = [];            // buffered lines of the current paragraph
  let list = null;          // { tag: "ul"|"ol", items: [] }
  const flushPara = () => { if (para.length) { out.push(`<p>${para.join("<br>")}</p>`); para = []; } };
  const flushList = () => {
    if (list) { out.push(`<${list.tag} class="pa-desc-list">${list.items.map((i) => `<li>${i}</li>`).join("")}</${list.tag}>`); list = null; }
  };
  const flushAll = () => { flushPara(); flushList(); };

  for (const rawLine of src.split("\n")) {
    const line = rawLine.trim();

    // Blank line — end the current paragraph/list.
    if (!line) { flushAll(); continue; }

    // Divider: a line of --- / *** / ___ .
    if (/^([-*_])\1{2,}$/.test(line)) { flushAll(); out.push('<hr class="pa-desc-hr">'); continue; }

    // Heading: # / ## / ### text .
    let m = /^#{1,3}\s+(.*)$/.exec(line);
    if (m) { flushAll(); out.push(`<div class="pa-desc-h">${inline(m[1])}</div>`); continue; }

    // Bullet / numbered list item (bullet requires the space so "**Hit**" isn't caught).
    m = /^[-*]\s+(.*)$/.exec(line);
    if (m) { flushPara(); if (!list || list.tag !== "ul") { flushList(); list = { tag: "ul", items: [] }; } list.items.push(inline(m[1])); continue; }
    m = /^\d+\.\s+(.*)$/.exec(line);
    if (m) { flushPara(); if (!list || list.tag !== "ol") { flushList(); list = { tag: "ol", items: [] }; } list.items.push(inline(m[1])); continue; }

    // Outcome line: only a leading **Hit** / **Miss** / **Contest** / **Threshold** / **Wound**
    // (letters compared, so "**Hit:**" still matches) starts a colored outcome block. Any OTHER
    // leading bold — a stat label, or a sentence that merely opens with emphasis — falls through to
    // a normal paragraph where inline() bolds it, so ordinary prose isn't hijacked into a label row.
    m = /^\*\*([^*]+)\*\*\s*([\s\S]*)$/.exec(line);
    if (m) {
      const cls = OUTCOME_CLASS[m[1].toLowerCase().replace(/[^a-z]/g, "")];
      if (cls) {
        flushAll();
        const rest = m[2] ? ` ${inline(m[2])}` : "";
        out.push(`<p class="pa-desc-line"><strong class="${cls}">${inline(m[1])}</strong>${rest}</p>`);
        continue;
      }
    }

    // Plain text — accumulate into the current paragraph (single newline → soft break).
    flushList();
    para.push(inline(line));
  }
  flushAll();
  return out.length ? `<div class="pa-desc-prose">${out.join("")}</div>` : "";
}

/** The display HTML for an item's description: legacy HTML passes through; markup is rendered.
 *  "" when blank. Async only for the Foundry enrich pass (resolves @links / [[rolls]]). */
export async function renderDescriptionHTML(item, { secrets = false } = {}) {
  const raw = (item?.system?.description ?? "").trim();
  if (!raw) return "";
  const body = isLegacyHTML(raw) ? raw : renderProse(raw);
  const TE = foundry.applications?.ux?.TextEditor?.implementation ?? globalThis.TextEditor;
  return TE.enrichHTML(body, { relativeTo: item, secrets, rollData: item?.getRollData?.() ?? {} });
}

/** Sync description block (no Foundry enrich) — for render paths that build HTML synchronously,
 *  like the actor-sheet command-row dropdown. Inline calc tokens still resolve (the resolver is
 *  sync); only Foundry's own @links / [[rolls]] stay literal here. "" when blank. */
export function renderDescriptionBlock(item) {
  const raw = (item?.system?.description ?? "").trim();
  if (!raw) return "";
  return resolveInlineCalcs(isLegacyHTML(raw) ? raw : renderProse(raw), item);
}

